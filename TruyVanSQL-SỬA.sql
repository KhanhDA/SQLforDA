
Select MANV, TENNV
From HOADON HD, NHANVIEN NV
Where HD.MANVLAP=NV.MANV and
TONGGIATRI>= ALL (SELECT MAX(TONGGIATRI)
			FROM HOADON);


Select NHANVIEN.MaLoai, TENLOAI, Count (MANV)
From LOAINV, NHANVIEN
Where LOAINV.MALOAI=NHANVIEN.MALOAI
Group by NHANVIEN.MaLoai, TENLOAI
Having Count( MANV) >= ALL (Select Count (MANV)
				From NHANVIEN
				Group by MALOAI );

Select SUM(TONGGIATRI)
From HOADON
Where (DAY(NGAYLAP)=11 AND MONTH(NGAYLAP)=6) OR (DAY(NGAYLAP)=12 AND MONTH(NGAYLAP)=6);

